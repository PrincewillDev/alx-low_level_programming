#include "main.h"

/**
 * _strcat - This function concatenate two strings
 *
 * @dest: The destination pointer parameter
 * @src: The source pointer parameter
 * char - The char datatype
 * Return: the destination pointer
 */

char *_strcat(char *dest, char *src)
{

	strcat(dest, src);

	return (dest);
}
