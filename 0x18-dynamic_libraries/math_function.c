#include "main.h"

/**
 * add - addition of numbers
 * @a: first arg
 * @b: second arg
 * Return: Sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction of numbers
 * @a: first arg
 * @b: second arg
 * Return: Subtract of a and b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication of numbers
 * @a: first arg
 * @b: second arg
 * Return: multiply of a and b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division of numbers
 * @a: first arg
 * @b: second arg
 * Return: divide of a and b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of numbers
 * @a: first arg
 * @b: second arg
 * Return: remainder of a and b
 */

int mod(int a, int b)
{
	return (a % b);
}
